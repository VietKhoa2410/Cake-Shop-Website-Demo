package model;

/**
 *
 * @author ASUS
 */
public class User {

    private String name, pass, brand, address, image, content;

    public User(String name, String pass, String brand, String address, String image, String content) {
        this.name = name;
        this.pass = pass;
        this.brand = brand;
        this.address = address;
        this.image = image;
        this.content = content;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

}
